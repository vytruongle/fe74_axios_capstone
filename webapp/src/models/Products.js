function Product(
  id,
  name,
  price,
  screen,
  blackCamera,
  frontCamera,
  img,
  desc,
  type,
  quantity
) {
  this.id = id;
  this.name = name;
  this.price = price;
  this.screen = screen;
  this.blackCamera = blackCamera;
  this.frontCamera = frontCamera;
  this.img = img;
  this.desc = desc;
  this.type = type;
  this.quantity = quantity;
}
